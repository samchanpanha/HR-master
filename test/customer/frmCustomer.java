    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        if(!clFunction.checkIfTextFieldEmpty(allTxt)){
            
            
            
            if(!clCustomer.update(txtId.getText(),txtName.getText(),cbGender.getSelectedItem()+"",txtPhone.getText())) return;
            
            String []st={txtId.getText(),txtName.getText(),cbGender.getSelectedItem()+"",txtPhone.getText()};
            
            for(int i=0;i<modelCustomer.getColumnCount();i++){
                modelCustomer.setValueAt(st[i],selectedRowIndex, i);
            }
            
            
            JOptionPane.showMessageDialog(null, "Edit successful", "",JOptionPane.INFORMATION_MESSAGE);
            
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if(!clCustomer.delete(txtId.getText())) return;
        
        
        modelCustomer.removeRow(selectedRowIndex);
        btnAdd.doClick();
        
        JOptionPane.showMessageDialog(null, "Delete successful", "",JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnDeleteActionPerformed


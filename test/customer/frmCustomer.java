    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        
        if(btnAdd.getText().equals("Cancel")){
            jTableCustomer.clearSelection();
            return;
        }
        

        if(!clFunction.checkIfTextFieldEmpty(allTxt)){

            if(!clCustomer.insert(txtName.getText(),cbGender.getSelectedItem()+"",txtPhone.getText())) return;
            
            
            String [] st={txtId.getText(),txtName.getText(),cbGender.getSelectedItem()+"",txtPhone.getText()};
            modelCustomer.addRow(st);
            clearForm();
            JOptionPane.showMessageDialog(null, "Add successful", "",JOptionPane.INFORMATION_MESSAGE);
            
        }
    }//GEN-LAST:event_btnAddActionPerformed


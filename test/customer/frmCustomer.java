    void changeButtonContext(){
        if(jTableCustomer.getSelectedRowCount()==1){
            btnAdd.setText("Cancel");
            btnEdit.setEnabled(true);
            btnDelete.setEnabled(true);
            
            getDataForEdit();
            
        }else{
            btnAdd.setText("Add");
            
            btnEdit.setEnabled(false);
            btnDelete.setEnabled(false);
            
            clearForm();
        }
    }
    
    
    int selectedRowIndex=-1;
    void getDataForEdit(){
        
        
        
        selectedRowIndex=jTableCustomer.getSelectedRow();
        
        txtId.setText(jTableCustomer.getValueAt(selectedRowIndex, 0)+"");
        txtName.setText(jTableCustomer.getValueAt(selectedRowIndex, 1)+"");
        txtPhone.setOriginValueAndSetTxt(jTableCustomer.getValueAt(selectedRowIndex, 3)+"");
        
        cbGender.setSelectedItem(jTableCustomer.getValueAt(selectedRowIndex, 2)+"");
        
    }
    
    void clearForm(){
        
        txtId.setText(clFunction.getLastId("customers"));
        clFunction.clearTextField(allTxt);
        cbGender.setSelectedIndex(0);
    }
    

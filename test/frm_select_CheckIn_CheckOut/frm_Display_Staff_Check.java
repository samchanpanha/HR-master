    private void DisplayName(){
          if (ckEnable.isSelected()==true) {
            cbstaff.setEnabled(true);
           String sql = "SELECT  Name FROM USERINFO;";
           c.DisplayName(cbstaff, sql);
           cbstaff.setSelectedItem("");
           ckEnable.setSelected(true);
          
        }
        else
        {
            cbstaff.setEnabled(false);
            cbstaff.removeAllItems();
            ckEnable.setSelected(false);
        }
    }

    private void DisplayCheckTime(){
        
        
        if (dstart.getDate()==null || dend.getDate()==null) {
                JOptionPane.showMessageDialog(this,"You Should Select Date For Search ");
                return;
            }
        SimpleDateFormat formater = new SimpleDateFormat("MM/dd/yyyy");
          String ds =formater.format(dstart.getDate());
          ds="#"+ds+"#";
          String de =formater.format(dend.getDate());
          de="#"+de+"#";
        try {
            
            
            if (ckEnable.isSelected()==true) {
               
                String sql ="SELECT USERID ,`Name` , CHECKTIME ,Switch(CHECKTYPE=\"I\",\"CheckIns\",CHECKTYPE=\"O\",\"CheckOut\") AS Check FROM CHECKINOUT  JOIN USERINFO ON CHECKINOUT.USERID=USERINFO.USERID  WHERE CHECKINOUT.USERID='"+Stid.getText()+"' AND Format (CHECKINOUT.CHECKTIME,\"Short Date\") BETWEEN Format ("+ds+",\"Short Date\") AND Format ("+de+",\"Short Date\");";
                 c.showDataInTable(tbCheck, sql, dm);
            }
            else{
                
                String sql="SELECT CHECKINOUT.USERID ,`Name` , CHECKTIME ,Switch(CHECKTYPE=\"I\",\"CheckIns\",CHECKTYPE=\"O\",\"CheckOut\") AS Check FROM CHECKINOUT  JOIN USERINFO ON CHECKINOUT.USERID=USERINFO.USERID  WHERE  Format (CHECKINOUT.CHECKTIME,\"Short Date\") BETWEEN Format ("+ds+",\"Short Date\") AND Format ("+de+",\"Short Date\");";
                c.showDataInTable(tbCheck, sql, dm);
            }
        } catch (Exception e) {
            e.getMessage();
        }
    }

